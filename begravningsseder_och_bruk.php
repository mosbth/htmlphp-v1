<h2>Förr i tiden</h2>
<p>Så var det förr:
<p>Begravningen skedde förr i tiden ganska snart efter dödsfallet och den döde bars från hemmet till 
graven. Kring mitten av 1800-talet blev det allt vanligare att man körde den
döde till kyrkan med häst och vagn.

<h2>Den första likvagnen i Hakarp</h2>
<p>Den första likvagnen i Hakarp tillverkades av Granlunds Vagnfabrik i Gränna. Fabriken var berömd för 
leveranser till hovet, Finland och Ryssland. Vagnarna gjordes helt för hand. Kyrkoherden Knut Anders 
Sandström (präst i Hakarp 1903-1923) skänkte vagnen till församlingen 1907. Vagnen användes i Hakarp mellan 
åren 1907 och 1948. Som ny kostade vagnen ca 700 kronor. Men då vagnen så småningom ansågs ”livsfarlig”
med dåliga bromsar överlämnades den till länsmuseet. För 1 krona inköptes Huskvarnaförsamlingens likvagn. 
I Huskvarna anskaffades en likbil år 1947. Den då inköpta likvagnen finns nu i Garpa skans 
(vid Huskvarna kyrkogård).

<h2>Begravningsbyråer</h2>
<p>Begravningsbyråer fanns till en början inte. Förberedelser inför begravningen sköttes av de anhöriga 
och alla i byn ställde upp och stöttade på olika sätt.
På 1880-talet fanns det bara tre begravningsfirmor i Stockholm, men redan kring sekelskiftet drygt tio. 
De svenska begravningsentreprenörerna organiserade sig 1922 i ett eget förbund.

<h2>Begravningstavlor - minnestavlor</h2>
<p>Bruket med begravningstavlor fick en allt större utbredning i början av 1800-talet och var vanligast 
i sydvästra Sverige, Halland, västra Småland och sydöstra Skåne. Seden upphörde under 1900-talets första 
decennier. Minnestavlorna består ofta av namn på den döde, födelsedatum, dödsdatum och en hyllningstext
i form av en vers som omges av kors, kistor, änglar, sorgträd, urnor mm.

<h2>Pärlkransar</h2>
Kransar gjorda av glaspärlor hängdes på gravkorset eller lades på graven. Den tunna tråden med glaspärlor 
formades till spiraler, blad eller blommor. Bruket kom ursprungligen från Italien och
Frankrike men började förekomma i Sverige under senare delen av 1800-talet. Man lade den på kistan i 
hemmet och sedan följde kransen kistan till gravsättningen, då man lade kransen på graven. Blommor var 
dyra och kunde vara svåra att anskaffa.
<p>Trots att metalltråden rostade och glaspärlorna kunde gå sönder av frosten varade kransarna längre 
än de av naturligt material. Efter en tid tog man hem kransen och förvarade den på loge eller vind för 
att användas vid nästa dödsfall. Först på 30-talet försvann de från kyrkogårdarna. På landsbygden kunde 
de dock ses ända in på 50- och 60-talen.

<h2>Gravöl</h2>
<p>Att ställa till med stort kalas i sorgehuset efter begravningen var förr en självklarhet. Det är 
först under 1900-talets gång som begravningar och gravöl omformats till minnesstunder i form av 
stillsamma och lågmälda samkväm, ofta i litet format. Tidigt på begravningsdagens morgon samlades alla 
inbjudna gäster i sorgehuset för att äta frukost. Efter jordfästningen och gudstjänsten var det åter 
dags att bege sig till sorgehuset. Det var nu den stora festen skulle börja. Festens form vid
ett gravöl skilde sig inte nämnvärt från ett bröllop i äldre tid.
<p>Gästerna skulle ha förning med sig. Vanliga matvaror i förningen var bröd, smör, ost, ägg, gröt, 
kött, fläsk, pudding och ostkakor. Gästerna skulle ha förning med sig tillbaka när bjudningen var över. 
Många gånger bestod den av smakbitar av de övriga gästernas förning men det förekom också att värdinnan 
ställde till med brödbak för att gästerna skulle få något med sig hem. Det var vanligt med stora 
begravningskringlor
– sockerkringlor – av vete eller saffran. I regel var de så väl tilltagna, att de kunde hängas på 
armen. Ju större, desto bättre blev anseendet för värdfolket och desto mer hedrades den döde.

<h2>Dödsannonser</h2>
Dödsannonser förekom i dagstidningarna först på 1850- talet. Från början hade de formen av ett kort 
och mycket formellt tillkännagivande. Längre fram blev det vanligt att tillfoga uppgifter om tidpunkt 
och plats för begravningen. I början av 1900-talet uppstod bruket att sätta ut de sörjandes förnamn i 
annonsen. Det blev också vanligt att referera till en bibel- eller psalmvers. På 1930-talet bortföll 
uträkningen av den avlidnes exakta ålder liksom uppgiften om
klockslaget då döden inträffade.
<p>Vid tiden kring sekelskiftet kunde man se de första korsen i annonserna, men först vid mitten på
1900-talet hade korssymbolen slagit igenom helt. Nyårsaftonen 1977 förekom den första
blomman i en dödsannons 
och idag har vi många symboler att välja bland, mer än 250 stycken. Valet av symbol motiveras oftast 
av att det ska passa in på den döda.

<h2>Klädsel</h2>
<p>Det ansågs allmänt att de anhöriga skulle sörja den döde ett år. Under den tiden skulle festliga 
tillställningar undvikas. Den sörjande borde också bära en särskild klädedräkt för att utåt visa sin 
sorg. Svart är av traditionen sorgens färg och den svarta sorgdräkten blev vanlig i slutet
av 1600-talet. Inom de borgerliga samhällsklasserna var frack och hög hatt långt fram i vår egen tid 
det vanliga sorgplagget för män. 
<p>Det mest slående inslaget
i den kvinnliga sorgdräkten har annars varit sorgslöjan, som de närmast anhöriga bar under hela
sorgetiden. Slöjans längd skulle stå i proportion till sorgens ”djup”. Den svarta, kvinnliga sorgdräkten 
kompletterades förr i tiden av vita förkläden, en sed som först brukats i samband med hovsorg. Men 
många hade inte fler plagg än som de bar. För dem blev sorgförklädet en möjlighet att klä sig i sorg. 
Högborgerliga kvinnor bar ”Smäck och gravor”, snibbmössor med flor, vid begravningar och kondoleansvisiter.


